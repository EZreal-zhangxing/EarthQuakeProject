package com.zx.Pojo;

/**
 * 团体类型
 */
public class TeamType {
    private Integer id;
    private String teamType;

    public TeamType() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTeamType() {
        return teamType;
    }

    public void setTeamType(String teamType) {
        this.teamType = teamType;
    }
}
